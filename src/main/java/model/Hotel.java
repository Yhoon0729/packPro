package model;

public class Hotel {
	private int htlId;
	private String fstLoc;
	private String htlName;
	private double htlGrade;
	private int htlPrice;
	private String htlImg;
	
	public int getHtlId() {
		return htlId;
	}
	public void setHtlId(int htlId) {
		this.htlId = htlId;
	}
	public String getFstLoc() {
		return fstLoc;
	}
	public void setFstLoc(String fstLoc) {
		this.fstLoc = fstLoc;
	}
	public String getHtlName() {
		return htlName;
	}
	public void setHtlName(String htlName) {
		this.htlName = htlName;
	}
	public double getHtlGrade() {
		return htlGrade;
	}
	public void setHtlGrade(double htlGrade) {
		this.htlGrade = htlGrade;
	}
	public int getHtlPrice() {
		return htlPrice;
	}
	public void setHtlPrice(int htlPrice) {
		this.htlPrice = htlPrice;
	}
	public String getHtlImg() {
		return htlImg;
	}
	public void setHtlImg(String htlImg) {
		this.htlImg = htlImg;
	}
	
	
}
