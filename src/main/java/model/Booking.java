package model;

public class Booking {
	private int act_id;
	private String fst_code;
	private int htl_bookingid;
	private int car_history_id;
	private int car_id;
	private int htl_id;
	
	public int getAct_id() {
		return act_id;
	}
	public void setAct_id(int act_id) {
		this.act_id = act_id;
	}
	public String getFst_code() {
		return fst_code;
	}
	public void setFst_code(String fst_code) {
		this.fst_code = fst_code;
	}
	public int getHtl_bookingid() {
		return htl_bookingid;
	}
	public void setHtl_bookingid(int htl_bookingid) {
		this.htl_bookingid = htl_bookingid;
	}
	public int getCar_history_id() {
		return car_history_id;
	}
	public void setCar_history_id(int car_history_id) {
		this.car_history_id = car_history_id;
	}
	public int getCar_id() {
		return car_id;
	}
	public void setCar_id(int car_id) {
		this.car_id = car_id;
	}
	public int getHtl_id() {
		return htl_id;
	}
	public void setHtl_id(int htl_id) {
		this.htl_id = htl_id;
	}
	
	
}
